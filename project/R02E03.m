clc;
close all;
clear;

t1 = linspace(-5, -3, 100);
x1 = -t1 + 1;
t2 = linspace(-3, -1, 100);
x2 = 4* ones(size(t2));
t3 = linspace(-1, 1, 200);
x3 = 4 * cos(2 * pi * t3);
t4 = linspace(1, 4, 150);
x4 = t4 + 3;
t5 = linspace(4, 5, 50);
x5 = (-3 * t5) + 19;
hold on;
plot(t1, x1,'b');
hold on;
plot(t2, x2,'r');
hold on;
plot(t3, x3,'g');
hold on;
plot(t4, x4,'m');
hold on;
plot(t5, x5,'k');
hold on;
xlabel('Time (t)');
ylabel('x(t)');
title('Piecewise Function x(t)');
grid on;
legend('Part 1', 'Part 2', 'Part 3', 'Part 4', 'Part 5');